import kotlinx.coroutines.flow.StateFlow

/**
 * Centralized localization for ScamSlayer.
 * Usage: L.s.homeTitle, L.s.recordings, etc.
 * Call L.setLanguage("en") to switch.
 */
object L {
    private val _current = MutableStateFlow(Strings.CS)
    val current: StateFlow<Strings> = _current

    val s: Strings get() = _current.value

    fun setLanguage(code: String) {
        _current.value = when (code) {
            "cs" -> Strings.CS
            "en" -> Strings.EN
            "ar" -> Strings.AR
            "da" -> Strings.DA
            "de" -> Strings.DE
            "es" -> Strings.ES
            "fi" -> Strings.FI
            "fr" -> Strings.FR
            "hi" -> Strings.HI
            "it" -> Strings.IT
            "ja" -> Strings.JA
            "ko" -> Strings.KO
            "nl" -> Strings.NL
            "no" -> Strings.NO
            "pl" -> Strings.PL
            "pt" -> Strings.PT
            "ru" -> Strings.RU
            "sk" -> Strings.SK
            "sv" -> Strings.SV
            "tr" -> Strings.TR
            "uk" -> Strings.UK
            "vi" -> Strings.VI
            "zh" -> Strings.ZH
            else -> {
                val sysLang = java.util.Locale.getDefault().language
                when (sysLang) {
                    "cs" -> Strings.CS
                    "en" -> Strings.EN
                    "ar" -> Strings.AR
                    "da" -> Strings.DA
                    "de" -> Strings.DE
                    "es" -> Strings.ES
                    "fi" -> Strings.FI
                    "fr" -> Strings.FR
                    "hi" -> Strings.HI
                    "it" -> Strings.IT
                    "ja" -> Strings.JA
                    "ko" -> Strings.KO
                    "nl" -> Strings.NL
                    "no" -> Strings.NO
                    "pl" -> Strings.PL
                    "pt" -> Strings.PT
                    "ru" -> Strings.RU
                    "sk" -> Strings.SK
                    "sv" -> Strings.SV
                    "tr" -> Strings.TR
                    "uk" -> Strings.UK
                    "vi" -> Strings.VI
                    "zh" -> Strings.ZH
                    else -> Strings.EN
                }
            }
        }
    }
}

